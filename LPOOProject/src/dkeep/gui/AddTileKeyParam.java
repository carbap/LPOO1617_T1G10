package dkeep.gui;

public class AddTileKeyParam {

	public char tableCharCopy;
	public int indexX;
	public int indexY;
	public int tableWidth;
	public int tableHeight;
	
	public AddTileKeyParam(char tableCharCopy, int indexX, int indexY, int tableWidth, int tableHeight) {
		super();
		this.tableCharCopy = tableCharCopy;
		this.indexX = indexX;
		this.indexY = indexY;
		this.tableWidth = tableWidth;
		this.tableHeight = tableHeight;
	}

	
	
}
